
RESET_HPPP_ROLLING:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDA #0
	STA @VIRTUAL02
	BRA @UNKNOWN4
@UNKNOWN0:
.IF .DEFINED(JPN)
	LDA @VIRTUAL02
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::party_members,X
.ELSE
	LDX @VIRTUAL02
	LDA GAME_STATE + game_state::party_members,X
.ENDIF
	AND #$00FF
	DEC
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)
	TAY
	LDA a:char_struct::afflictions,Y
	AND #$00FF
	CMP #1
	BEQ @UNKNOWN1
	LDA a:char_struct::current_hp,Y
	BNE @UNKNOWN1
	LDA #1
	STA a:char_struct::current_hp_target,Y
@UNKNOWN1:
	LDA a:char_struct::current_hp_fraction,Y
	BEQ @UNKNOWN2
	LDA a:char_struct::current_hp,Y
	STA @LOCAL00
	TYA
	CLC
	ADC #char_struct::current_hp_target
	TAX
	LDA __BSS_START__,X
	STA @VIRTUAL04
	LDA @LOCAL00
	CMP @VIRTUAL04
	BLTEQ @UNKNOWN2
	STA __BSS_START__,X
@UNKNOWN2:
	LDA a:char_struct::current_pp_fraction,Y
	BEQ @UNKNOWN3
	LDA a:char_struct::current_pp,Y
	STA @LOCAL00
	TYA
	CLC
	ADC #char_struct::current_pp_target
	TAX
	LDA __BSS_START__,X
	STA @VIRTUAL04
	LDA @LOCAL00
	CMP @VIRTUAL04
	BLTEQ @UNKNOWN3
	STA __BSS_START__,X
@UNKNOWN3:
	INC @VIRTUAL02
@UNKNOWN4:
	LDA GAME_STATE+game_state::player_controlled_party_count
	AND #$00FF
	STA @VIRTUAL04
	LDA @VIRTUAL02
	CMP @VIRTUAL04
	BCCL @UNKNOWN0
	SEP #PROC_FLAGS::ACCUM8
	LDA #1
	STA FASTEST_HPPP_METER_SPEED
	REP #PROC_FLAGS::ACCUM8
	END_C_FUNCTION
