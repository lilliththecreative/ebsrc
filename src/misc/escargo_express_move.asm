
ESCARGO_EXPRESS_MOVE:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	STX @VIRTUAL02
	TAY
	STY @LOCAL00
	LDX @VIRTUAL02
	TYA
	JSL GET_CHARACTER_ITEM
	JSR ESCARGO_EXPRESS_STORE
	CMP #FALSE
	BEQ @RETURN_ZERO
	LDX @VIRTUAL02
	LDY @LOCAL00
	TYA
	JSR REMOVE_ITEM_FROM_INVENTORY
	BRA @RETURN
@RETURN_ZERO:
	LDA #FALSE
@RETURN:
	END_C_FUNCTION
