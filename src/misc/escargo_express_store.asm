
ESCARGO_EXPRESS_STORE:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	TAY
	LDA #0
	STA @LOCAL00
	BRA @LOOP_ENTRY
@LOOP_BEGIN:
	LDA @LOCAL00
	CLC
.IF .DEFINED(JPN)
	ADC #.LOWORD(GAME_STATE)
	CLC
	ADC #game_state::escargo_express_items
.ELSE
	ADC #.LOWORD(GAME_STATE)+game_state::escargo_express_items
.ENDIF
	TAX
	LDA __BSS_START__,X
	AND #$00FF
	BNE @ENTRY_FILLED
	TYA
	SEP #PROC_FLAGS::ACCUM8
	STA __BSS_START__,X
	REP #PROC_FLAGS::ACCUM8
	TYA
	BRA @RETURN
@ENTRY_FILLED:
	LDA @LOCAL00
	INC
	STA @LOCAL00
@LOOP_ENTRY:
	STA @VIRTUAL02
	LDA #.SIZEOF(game_state::escargo_express_items)
	CLC
	SBC @VIRTUAL02
	BRANCHGTS @LOOP_BEGIN
	LDA #FALSE
@RETURN:
	END_C_FUNCTION
