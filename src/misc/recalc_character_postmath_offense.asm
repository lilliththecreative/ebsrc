
RECALC_CHARACTER_POSTMATH_OFFENSE:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT8
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;NEAR int character
	END_STACK_VARS
	TAX
	DEC
	STA @VIRTUAL02
	STA @LOCAL04
	LDA @VIRTUAL02
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	TAX
	LDA PARTY_CHARACTERS+char_struct::base_offense,X
	AND #$00FF
	TAY
	STY @LOCAL03
	LDA PARTY_CHARACTERS+char_struct::equipment+EQUIPMENT_SLOT::WEAPON,X
	AND #$00FF
	STA @LOCAL02
	BEQ @UNKNOWN1
	LDX #0
	STX @LOCAL01
	LDA @VIRTUAL02
	CMP #PARTY_MEMBER::POO - 1
	BNE @UNKNOWN0
	LDX #1
	STX @LOCAL01
@UNKNOWN0:
	LDA @LOCAL02
	DEC
.IF .DEFINED(JPN)
	STA @VIRTUAL04
.ELSE
	PHA
.ENDIF
	LDA @VIRTUAL02
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	CLC
	ADC #.LOWORD(PARTY_CHARACTERS)+char_struct::items
.IF .DEFINED(JPN)
	CLC
	ADC @VIRTUAL04
.ELSE
	PLY
	STY @VIRTUAL02
	CLC
	ADC @VIRTUAL02
.ENDIF
	TAX
	LDA __BSS_START__,X
	AND #$00FF
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(item)
	LDX @LOCAL01
	STX @VIRTUAL02
	CLC
	ADC @VIRTUAL02
	CLC
	ADC #item::params + item_parameters::strength
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA f:ITEM_CONFIGURATION_TABLE,X
	REP #PROC_FLAGS::ACCUM8
	SEC
	AND #$00FF
	SBC #$0080
	EOR #$FF80
	STA @VIRTUAL04
	LDY @LOCAL03
	TYA
	CLC
	ADC @VIRTUAL04
	TAY
@UNKNOWN1:
	STY @VIRTUAL04
	LDA #0
	CLC
	SBC @VIRTUAL04
	BRANCHLTEQS @UNKNOWN4
	LDX #0
	BRA @UNKNOWN8
@UNKNOWN4:
	TYA
	CLC
	SBC #$00FF
	BRANCHLTEQS @UNKNOWN7
	LDX #$00FF
	BRA @UNKNOWN8
@UNKNOWN7:
	TYA
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	TAX
@UNKNOWN8:
	TXA
	SEP #PROC_FLAGS::ACCUM8
	STA @LOCAL00
	REP #PROC_FLAGS::ACCUM8
	LDA @LOCAL04
	STA @VIRTUAL02
	LDY #.SIZEOF(char_struct)
	JSL MULT168
	TAX
	SEP #PROC_FLAGS::ACCUM8
	LDA @LOCAL00
	STA PARTY_CHARACTERS+char_struct::offense,X
	END_C_FUNCTION
