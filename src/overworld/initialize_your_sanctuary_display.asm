
INITIALIZE_YOUR_SANCTUARY_DISPLAY:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	STZ NEXT_YOUR_SANCTUARY_LOCATION_TILE_INDEX
	STZ TOTAL_YOUR_SANCTUARY_LOADED_TILESET_TILES
	STZ YOUR_SANCTUARY_LOADED_TILESET_TILES
	STZ LOADED_ANIMATED_TILE_COUNT
	STZ MAP_PALETTE_ANIMATION_LOADED
	LDA #0
	STA @LOCAL00
	BRA @UNKNOWN1
@UNKNOWN0:
	ASL
	TAX
	STZ LOADED_YOUR_SANCTUARY_LOCATIONS,X
	LDA @LOCAL00
	INC
	STA @LOCAL00
@UNKNOWN1:
	CMP #8
	BCC @UNKNOWN0
	SEP #PROC_FLAGS::ACCUM8
	LDA #$10
	STA TM_MIRROR
	REP #PROC_FLAGS::ACCUM8
	END_C_FUNCTION
