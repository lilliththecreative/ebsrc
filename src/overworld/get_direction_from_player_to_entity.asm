
GET_DIRECTION_FROM_PLAYER_TO_ENTITY:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_RETURN_INT16 ;int
	END_STACK_VARS
	LDA CURRENT_ENTITY_SLOT
	ASL
	STA @LOCAL02
	LDA GAME_STATE + game_state::leader_y_coord
	STA @LOCAL00
	LDY GAME_STATE + game_state::leader_x_coord
	LDA @LOCAL02
	TAX
	LDA ENTITY_ABS_Y_TABLE,X
	TAX
	STX @LOCAL01
	LDA @LOCAL02
	TAX
	LDA ENTITY_ABS_X_TABLE,X
	LDX @LOCAL01
	JSL GET_DIRECTION_TO
	END_C_FUNCTION
