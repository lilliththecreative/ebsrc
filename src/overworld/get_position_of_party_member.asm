
GET_POSITION_OF_PARTY_MEMBER:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	TAX
	LDY CURRENT_ENTITY_SLOT
	STY @LOCAL02
	CPX #<-2
	BNE @UNKNOWN0
	LDA GAME_STATE+game_state::party_count
	AND #$00FF
	TAX
	STX @LOCAL01
	TXA
	DEC
	ASL
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::unknownA2,X
.ELSE
	TAX
	LDA GAME_STATE + game_state::unknownA2,X
.ENDIF
	STA @LOCAL00
	ASL
	TAX
	LDA ENTITY_ABS_X_TABLE,X
	BNE @UNKNOWN1
	LDX @LOCAL01
	TXA
	DEC
	DEC
	ASL
.IF .DEFINED(JPN)
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	LDA a:game_state::unknownA2,X
.ELSE
	TAX
	LDA GAME_STATE + game_state::unknownA2,X
.ENDIF
	STA @LOCAL00
	BRA @UNKNOWN1
@UNKNOWN0:
	TXA
	SEP #PROC_FLAGS::ACCUM8
	JSL UNKNOWN_C4608C
	STA @LOCAL00
@UNKNOWN1:
	LDY @LOCAL02
	TYA
	ASL
	TAY
	LDA @LOCAL00
	ASL
	TAX
	LDA ENTITY_ABS_X_TABLE,X
	STA ENTITY_SCRIPT_VAR6_TABLE,Y
	LDA ENTITY_ABS_Y_TABLE,X
	STA ENTITY_SCRIPT_VAR7_TABLE,Y
	END_C_FUNCTION
