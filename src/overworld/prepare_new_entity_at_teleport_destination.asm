
PREPARE_NEW_ENTITY_AT_TELEPORT_DESTINATION: ;$C46DE5
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE 15
	SEP #PROC_FLAGS::ACCUM8
	STA $0E
	REP #PROC_FLAGS::ACCUM8
	LOADPTR TELEPORT_DESTINATION_TABLE, $06
	LDA $0E
	AND #$00FF
	OPTIMIZED_MULT $04, .SIZEOF(teleport_destination)
	CLC
	ADC $06
	STA $06
	STA $0A
	LDA $08
	STA $0C
	LDA [$0A] ;teleport_destination::x_coord
	ASL
	ASL
	ASL
	STA ENTITY_PREPARED_X_COORDINATE
	LDY #teleport_destination::y_coord
	LDA [$06],Y
	ASL
	ASL
	ASL
	STA ENTITY_PREPARED_Y_COORDINATE
	SEP #PROC_FLAGS::ACCUM8
	LDY #teleport_destination::direction
	LDA [$06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	DEC
	STA ENTITY_PREPARED_DIRECTION
	PLD
	RTL
