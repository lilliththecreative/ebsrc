
GET_SCREEN_TRANSITION_SOUND_EFFECT:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int transitionID
	STACK_RESERVE_PARAM_INT16 ;int getStart
	STACK_RESERVE_RETURN_INT16 ;int
	END_STACK_VARS
	STA @LOCAL00
	LOADPTR SCREEN_TRANSITION_CONFIG_TABLE, @VIRTUAL06
	LDA @LOCAL00
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(screen_transition_config)
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	CPX #0
	BNE @UNKNOWN0
	SEP #PROC_FLAGS::ACCUM8
	LDY #screen_transition_config::ending_sound_effect
	LDA [@VIRTUAL06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
	BRA @UNKNOWN1
@UNKNOWN0:
	SEP #PROC_FLAGS::ACCUM8
	LDY #screen_transition_config::start_sound_effect
	LDA [@VIRTUAL06],Y
	REP #PROC_FLAGS::ACCUM8
	AND #$00FF
@UNKNOWN1:
	END_C_FUNCTION
