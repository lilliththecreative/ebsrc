
TEST_YOUR_SANCTUARY_DISPLAY: ;$C4E366
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	.IF .DEFINED(JPN)
		RESERVE_STACK_SPACE_CLOBBER 16
		JSL INITIALIZE_YOUR_SANCTUARY_DISPLAY
		LDX #$0000
		STX $0E
	@UNKNOWN0:
		STZ BG1_Y_POS
		STZ BG1_X_POS
		JSL UPDATE_SCREEN
		JSL WAIT_UNTIL_NEXT_FRAME
		LDA PAD_STATE
		AND #PAD::R_BUTTON
		BNE @UNKNOWN0
		LDX $0E
		TXA
		JSL DISPLAY_YOUR_SANCTUARY_LOCATION
		JSL ENABLE_YOUR_SANCTUARY_DISPLAY
		LDX $0E
		INX
		STX $0E
		CPX #$0008
		BNE @UNKNOWN0
		LDX #$0000
		STX $0E
		BRA @UNKNOWN0
	.ELSE
		RTL
	.ENDIF
