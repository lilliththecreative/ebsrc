
DISABLE_HOTSPOT:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int id
	END_STACK_VARS
	TAX
	DEX
	STX @LOCAL00
	TXA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(active_hotspot)
	CLC
	ADC #.LOWORD(ACTIVE_HOTSPOTS)
	TAX
	LDA #0
	STA a:active_hotspot::mode,X
	LDX @LOCAL00
.IF .DEFINED(JPN)
	TXA
	CLC
	ADC #.LOWORD(GAME_STATE)
	TAX
	SEP #PROC_FLAGS::ACCUM8
	STZ a:game_state::active_hotspot_modes,X
.ELSE
	SEP #PROC_FLAGS::ACCUM8
	STZ GAME_STATE + game_state::active_hotspot_modes,X
.ENDIF
	REP #PROC_FLAGS::ACCUM8
	END_C_FUNCTION
