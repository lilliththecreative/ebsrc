
DEBUG_SET_CHAR_LEVEL:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	END_STACK_VARS
	JSR SET_INSTANT_PRINTING
	CREATE_WINDOW_NEAR #WINDOW::FILE_SELECT_MENU
	LDA #2
	JSR NUM_SELECT_PROMPT
	LDA @VIRTUAL06
	STA @VIRTUAL04
	MOVE_INT_CONSTANT NULL, @VIRTUAL06
	MOVE_INT @VIRTUAL06, @LOCAL00
	MOVE_INT @VIRTUAL06, @LOCAL01
	LDX #1
	TXA
	JSR CHAR_SELECT_PROMPT
	STA @VIRTUAL02
	CMP #0
	BEQ @UNKNOWN0
	LDY #1
	LDX @VIRTUAL04
	LDA @VIRTUAL02
	JSR RESET_CHAR_LEVEL_ONE
	LDY #0
	LDX #100
	LDA @VIRTUAL02
	JSR RECOVER_HP_AMTPERCENT
	LDY #0
	LDX #100
	LDA @VIRTUAL02
	JSR RECOVER_PP_AMTPERCENT
@UNKNOWN0:
	LDA #WINDOW::FILE_SELECT_MENU
	JSR CLOSE_WINDOW
	END_C_FUNCTION
