
DEBUG_SET_CHAR_LEVEL: ;$C13E7A
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE_CLOBBER 22
	JSL SET_INSTANT_PRINTING
	CREATE_WINDOW_NEAR #WINDOW::FILE_SELECT_MENU
	LDA #$0002
	JSR NUM_SELECT_PROMPT
	LDA $06
	STA $04
	MOVE_INT_CONSTANT NULL, $06
	MOVE_INT $06, $0E
	MOVE_INT $06, $12
	LDX #$0001
	TXA
	JSR CHAR_SELECT_PROMPT
	STA $02
	CMP #$0000
	BEQ @UNKNOWN0
	LDY #$0001
	LDX $04
	LDA $02
	JSR RESET_CHAR_LEVEL_ONE
	LDY #$0000
	LDX #$0064
	LDA $02
	JSR RECOVER_HP_AMTPERCENT
	LDY #$0000
	LDX #$0064
	LDA $02
	JSR RECOVER_PP_AMTPERCENT
@UNKNOWN0:
	LDA #$0014
	JSL CLOSE_WINDOW
	PLD
	RTL
