
CC_1C_14: ;$C1516B
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE 20
	STX $12
	LDX CURRENT_ATTACKER
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	CMP #$0001
	BNE @HANDLE_ALLY
	LDX $12
	CPX #$0001
	BEQ @RETURN_ENEMY_GENDER
	LDA ENEMIES_IN_BATTLE
	CMP #$0003
	BLTEQ @THREE_OR_FEWER_ENEMIES
	LDX #$0003
	BRA @RETURN_CAPPED_ENEMY_COUNT
@THREE_OR_FEWER_ENEMIES:
	LDX ENEMIES_IN_BATTLE
@RETURN_CAPPED_ENEMY_COUNT:
	TXA
	BRA @RETURN
@RETURN_ENEMY_GENDER:
	LDX CURRENT_ATTACKER
	LDA __BSS_START__,X
	LDY #.SIZEOF(enemy_data)
	JSL MULT168
	CLC
	ADC #enemy_data::gender
	TAX
	LDA f:ENEMY_CONFIGURATION_TABLE,X
	AND #$00FF
	BRA @RETURN
@HANDLE_ALLY:
	LDX $12
	CPX #$0001
	BEQ @RETURN_ALLY_GENDER
	JSL UNKNOWN_C2272F
	TAX
	CPX #$0003
	BLTEQ @THREE_OR_FEWER_ALLIES
	LDX #$0003
@THREE_OR_FEWER_ALLIES:
	TXA
	BRA @RETURN
@RETURN_ALLY_GENDER:
	LDX CURRENT_ATTACKER
	LDA a:battler::id,X
	CMP #$0002
	BNE @NOT_PAULA
	LDA #$0002
	BRA @RETURN
@NOT_PAULA:
	LDA #$0001
@RETURN:
	STORE_INT1632 $06
	MOVE_INT $06, $0E
	JSR SET_WORKING_MEMORY
	LDA #NULL
	PLD
	RTS
