
FILL_CHARACTER_PP_TILE_BUFFER:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int id
	STACK_RESERVE_PARAM_INT16 ;NEAR char* afflictions
	STACK_RESERVE_PARAM_INT16 ;int integer
	STACK_RESERVE_PARAM_INT16 ;int fraction
	END_STACK_VARS
	STY @VIRTUAL04
	STA @VIRTUAL02
	LDY @PARAM03
	STY @LOCAL00
	LDA __BSS_START__ + STATUS_GROUP::CONCENTRATION,X
	AND #$00FF
	BEQ @CAN_CONCENTRATE
	LDA @VIRTUAL02
	JSR FILL_HP_PP_TILE_BUFFER_X
	BRA @RETURN
@CAN_CONCENTRATE:
	LDA @VIRTUAL04
	JSR SEPARATE_DECIMAL_DIGITS
	LDY @LOCAL00
	LDX #1
	LDA @VIRTUAL02
	JSR FILL_HP_PP_TILE_BUFFER
@RETURN:
	END_C_FUNCTION
