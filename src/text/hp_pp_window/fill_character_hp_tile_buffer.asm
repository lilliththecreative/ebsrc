
FILL_CHARACTER_HP_TILE_BUFFER:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int id
	STACK_RESERVE_PARAM_INT16 ;int integer
	STACK_RESERVE_PARAM_INT16 ;int fraction
	END_STACK_VARS
	STY @LOCAL00
	STA @VIRTUAL02
	TXA
	JSR SEPARATE_DECIMAL_DIGITS
	LDY @LOCAL00
	LDX #0
	LDA @VIRTUAL02
	JSR FILL_HP_PP_TILE_BUFFER
	END_C_FUNCTION
