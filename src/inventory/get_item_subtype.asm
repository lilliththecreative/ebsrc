
GET_ITEM_SUBTYPE: ;$C224E1
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	LDY #.SIZEOF(item)
	JSL MULT168
	CLC
	ADC #item::type
	TAX
	LDA f:ITEM_CONFIGURATION_TABLE,X
	AND #$00FF
	AND #$000C
	BEQ @UNKNOWN0
	CMP #$0004
	BEQ @UNKNOWN1
	CMP #$0008
	BEQ @UNKNOWN2
	CMP #$000C
	BEQ @UNKNOWN3
	BRA @UNKNOWN4
@UNKNOWN0:
	LDA #$0001
	BRA @UNKNOWN5
@UNKNOWN1:
	LDA #$0002
	BRA @UNKNOWN5
@UNKNOWN2:
	LDA #$0003
	BRA @UNKNOWN5
@UNKNOWN3:
	LDA #$0004
	BRA @UNKNOWN5
@UNKNOWN4:
	LDA #$0000
@UNKNOWN5:
	RTL
