
DETERMINE_DODGE:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	LDX CURRENT_TARGET
	LDA a:battler::afflictions + STATUS_GROUP::PERSISTENT_EASYHEAL,X
	AND #$00FF
	CMP #STATUS_0::PARALYZED
	BNE @UNKNOWN0
	LDA #0
	BRA @UNKNOWN8
@UNKNOWN0:
	LDX CURRENT_TARGET
	LDA a:battler::afflictions + STATUS_GROUP::TEMPORARY,X
	AND #$00FF
	CMP #STATUS_2::ASLEEP
	BNE @UNKNOWN1
	LDA #0
	BRA @UNKNOWN8
@UNKNOWN1:
	CMP #STATUS_2::IMMOBILIZED
	BNE @UNKNOWN2
	LDA #0
	BRA @UNKNOWN8
@UNKNOWN2:
	CMP #STATUS_2::SOLIDIFIED
	BNE @UNKNOWN3
	LDA #0
	BRA @UNKNOWN8
@UNKNOWN3:
	LDX CURRENT_TARGET
	LDA a:battler::speed,X
	ASL
	LDX CURRENT_ATTACKER
	SEC
	SBC a:battler::speed,X
	STA @LOCAL00
	STA @VIRTUAL02
	LDA #0
	CLC
	SBC @VIRTUAL02
	BRANCHGTS @UNKNOWN6
	LDA @LOCAL00
	JSR SUCCESS_500
	CMP #0
	BNE @UNKNOWN7
@UNKNOWN6:
	LDA #0
	BRA @UNKNOWN8
@UNKNOWN7:
	LDA #1
@UNKNOWN8:
	END_C_FUNCTION
