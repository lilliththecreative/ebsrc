
GET_SHIELD_TARGETTING: ;$C23FEA
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	CMP #BATTLE_ACTIONS::PSI_SHIELD_SIGMA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_SHIELD_OMEGA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_PSI_SHIELD_SIGMA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_PSI_SHIELD_OMEGA
	BNE @MULTITARGET
@SINGLETARGET:
	LDA #$0001
	BRA @RETURN
@MULTITARGET:
	LDA #$0000
@RETURN:
	RTL
