
GET_SHIELD_TARGETTING:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_PARAM_INT16 ;int action
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	CMP #BATTLE_ACTIONS::PSI_SHIELD_SIGMA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_SHIELD_OMEGA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_PSI_SHIELD_SIGMA
	BEQ @SINGLETARGET
	CMP #BATTLE_ACTIONS::PSI_PSI_SHIELD_OMEGA
	BNE @MULTITARGET
@SINGLETARGET:
	LDA #1
	BRA @RETURN
@MULTITARGET:
	LDA #0
@RETURN:
	END_C_FUNCTION
