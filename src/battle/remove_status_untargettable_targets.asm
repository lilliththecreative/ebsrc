
REMOVE_STATUS_UNTARGETTABLE_TARGETS:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDX #0
	STX @LOCAL00
	BRA @UNKNOWN1
@UNKNOWN0:
	LDX CURRENT_ATTACKER
	CMP a:battler::current_action,X
	BEQ @UNKNOWN6
	LDX @LOCAL00
	INX
	STX @LOCAL00
@UNKNOWN1:
	TXA
	ASL
	TAX
	LDA f:DEAD_TARGETTABLE_ACTIONS,X
	BNE @UNKNOWN0
	LDY #0
	STY @LOCAL00
	BRA @UNKNOWN5
@UNKNOWN2:
	TYA
	JSL IS_CHAR_TARGETTED
	CMP #0
	BEQ @UNKNOWN4
	LDY @LOCAL00
	TYA
	LDY #.SIZEOF(battler)
	JSL MULT168
	TAX
	LDA BATTLERS_TABLE+battler::consciousness,X
	AND #$00FF
	BEQ @UNKNOWN3
	LDA BATTLERS_TABLE+battler::afflictions,X
	AND #$00FF
	TAX
	CPX #STATUS_0::UNCONSCIOUS
	BEQ @UNKNOWN3
	CPX #STATUS_0::DIAMONDIZED
	BNE @UNKNOWN4
@UNKNOWN3:
	LDY @LOCAL00
	TYA
	JSL REMOVE_TARGET
@UNKNOWN4:
	LDY @LOCAL00
	INY
	STY @LOCAL00
@UNKNOWN5:
	CPY #BATTLER_COUNT
	BCC @UNKNOWN2
@UNKNOWN6:
	END_C_FUNCTION
