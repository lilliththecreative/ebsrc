
SWAP_ATTACKER_WITH_TARGET: ;$C27E8A
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE_CLOBBER 16
	LDA CURRENT_ATTACKER
	STA $0E
	LDA CURRENT_TARGET
	STA CURRENT_ATTACKER
	LDA $0E
	STA CURRENT_TARGET
	LDA #$0000
	JSL FIX_ATTACKER_NAME
	JSL FIX_TARGET_NAME
	PLD
	RTS
