
CHECK_IF_VALID_TARGET:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_PARAM_INT16 ;int
	STACK_RESERVE_RETURN_INT16 ;bool
	END_STACK_VARS
	LDY #.SIZEOF(battler)
	JSL MULT168
	TAX
	LDA BATTLERS_TABLE+battler::consciousness,X
	AND #$00FF
	BEQ @INVALID
	LDA BATTLERS_TABLE+battler::npc_id,X
	AND #$00FF
	BNE @INVALID
	LDA BATTLERS_TABLE+battler::afflictions,X
	AND #$00FF
	CMP #STATUS_0::UNCONSCIOUS
	BEQ @INVALID
	CMP #STATUS_0::DIAMONDIZED
	BEQ @INVALID
	LDA #$0001
	BRA @RETURN
@INVALID:
	LDA #$0000
@RETURN:
	END_C_FUNCTION
