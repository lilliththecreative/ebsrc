
BTLACT_RAINBOW_OF_COLOURS:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	END_STACK_VARS
	LDX CURRENT_ATTACKER
	LDA a:battler::sprite_x,X
	AND #$00FF
	STA @VIRTUAL02
	LDX CURRENT_ATTACKER
	LDA a:battler::sprite_y,X
	AND #$00FF
	TAY
	STY @LOCAL01
	LDX CURRENT_ATTACKER
	STX @LOCAL00
	LDX CURRENT_ATTACKER
	LDA a:battler::current_action_argument,X
	AND #$00FF
	LDX @LOCAL00
	JSL BATTLE_INIT_ENEMY_STATS
	LDA @VIRTUAL02
	SEP #PROC_FLAGS::ACCUM8
	LDX CURRENT_ATTACKER
	STA a:battler::sprite_x,X
	LDY @LOCAL01
	REP #PROC_FLAGS::ACCUM8
	TYA
	SEP #PROC_FLAGS::ACCUM8
	LDX CURRENT_ATTACKER
	STA a:battler::sprite_y,X
	LDX CURRENT_ATTACKER
	REP #PROC_FLAGS::ACCUM8
	LDA __BSS_START__,X
	JSR UNKNOWN_C2F09F
	SEP #PROC_FLAGS::ACCUM8
	LDX CURRENT_ATTACKER
	STA a:battler::vram_sprite_index,X
	LDA #1
	LDX CURRENT_ATTACKER
	STA a:battler::has_taken_turn,X
	REP #PROC_FLAGS::ACCUM8
	LDA #1
	STA SKIP_DEATH_TEXT_AND_CLEANUP
	END_C_FUNCTION
