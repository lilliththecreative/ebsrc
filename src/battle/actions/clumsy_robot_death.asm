
BTLACT_CLUMSYDEATH:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	END_STACK_VARS
	LDA f:PSI_TELEPORT_DEST_TABLE+.SIZEOF(psi_teleport_destination) * 13 + psi_teleport_destination::event_flag
	JSL GET_EVENT_FLAG
	CMP #0
	BEQ @UNKNOWN0
	DISPLAY_BATTLE_TEXT_PTR MSG_BTL_TONZURA_BREAK_IN_OK
	SEP #PROC_FLAGS::ACCUM8
	LDA #TELEPORT_STYLE::INSTANT
	STA @LOCAL00
	LDA #15
	JSL SET_TELEPORT_STATE
	.A16
	BRA @UNKNOWN1
@UNKNOWN0:
	DISPLAY_BATTLE_TEXT_PTR MSG_BTL_TONZURA_BREAK_IN_NG
	SEP #PROC_FLAGS::ACCUM8
	LDA #TELEPORT_STYLE::INSTANT
	STA @LOCAL00
	LDA #13
	JSL SET_TELEPORT_STATE
	.A16
	LDA #1
	STA SPECIAL_DEFEAT
@UNKNOWN1:
	END_C_FUNCTION
