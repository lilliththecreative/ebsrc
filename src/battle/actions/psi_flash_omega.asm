	CMP #0
	BEQ @UNKNOWN4
	JSL RAND
	AND #$0007
	BEQ @UNKNOWN0
	CMP #1
	BEQ @UNKNOWN0
	CMP #2
	BEQ @UNKNOWN0
	CMP #3
	BEQ @UNKNOWN1
	CMP #4
	BEQ @UNKNOWN2
	BRA @UNKNOWN3
@UNKNOWN0:
	LDA CURRENT_TARGET
	JSL KO_TARGET
	BRA @UNKNOWN4
@UNKNOWN1:
	JSR FLASH_INFLICT_PARALYSIS
	BRA @UNKNOWN4
@UNKNOWN2:
