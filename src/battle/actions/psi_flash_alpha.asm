
BTLACT_PSI_FLASH_A:
	BEGIN_C_FUNCTION_FAR
	END_STACK_VARS
	JSR FAIL_ATTACK_ON_NPCS
	CMP #0
	BNE @UNKNOWN2
	JSR FLASH_IMMUNITY_TEST
	CMP #0
	BEQ @UNKNOWN1
	JSL RAND
	AND #$0007
	BNE @UNKNOWN0
	JSR FLASH_INFLICT_FEELING_STRANGE
	BRA @UNKNOWN1
@UNKNOWN0:
	JSR FLASH_INFLICT_CRYING
@UNKNOWN1:
	JSR WEAKEN_SHIELD
@UNKNOWN2:
	END_C_FUNCTION
