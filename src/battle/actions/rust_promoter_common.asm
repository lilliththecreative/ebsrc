
RUST_SPRAY_COMMON: ;$C2AA1E
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE 20
	TAX
	STX $12
	JSR SUCCESS_LUCK80
	CMP #$0000
	BEQ @FAILURE
	LDX CURRENT_TARGET
	LDA a:battler::ally_or_enemy,X
	AND #$00FF
	CMP #$0001
	BNE @FAILURE
	LDX CURRENT_TARGET
	LDA a:battler::id,X
	JSR GET_ENEMY_TYPE
	CMP #$0002
	BNE @FAILURE
	LDX $12
	TXA
	JSR FIFTY_PERCENT_VARIANCE
	LDX #$00FF
	JSR CALC_RESIST_DAMAGE
	BRA @RETURN
@FAILURE:
	DISPLAY_BATTLE_TEXT_PTR MSG_BTL_KIKANAI
@RETURN:
	PLD
	RTS
