
BTLACT_REDUCEOFFDEF: ;$C28F21
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	RESERVE_STACK_SPACE_CLOBBER 24
	JSR FAIL_ATTACK_ON_NPCS
	CMP #$0000
	BNE @UNKNOWN0
	LDX CURRENT_TARGET
	LDY a:battler::offense,X
	STY $16
	LDA CURRENT_TARGET
	JSR HEXADECIMATE_OFFENSE
	LOADPTR MSG_BTL_OFFENSE_DOWN, $0E
	LDX CURRENT_TARGET
	LDY $16
	TYA
	SEC
	SBC a:battler::offense,X
	STORE_INT1632 $06
	MOVE_INT $06, $12
	JSL DISPLAY_TEXT_WAIT
	LDX CURRENT_TARGET
	LDY a:battler::defense,X
	STY $16
	LDA CURRENT_TARGET
	JSR HEXADECIMATE_DEFENSE
	LOADPTR MSG_BTL_DEFENSE_DOWN, $0E
	LDX CURRENT_TARGET
	LDY $16
	TYA
	SEC
	SBC a:battler::defense,X
	STORE_INT1632 $06
	MOVE_INT $06, $12
	JSL DISPLAY_TEXT_WAIT
@UNKNOWN0:
	PLD
	RTL
