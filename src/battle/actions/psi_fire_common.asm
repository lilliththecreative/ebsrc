
PSI_FIRE_COMMON:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int
	END_STACK_VARS
	TAX
	STX @LOCAL00
	JSR PSI_SHIELD_NULLIFY
	CMP #0
	BNE @UNKNOWN0
	LDX @LOCAL00
	TXA
	JSR TWENTY_FIVE_PERCENT_VARIANCE
	STA @LOCAL00
	LDX CURRENT_TARGET
	LDA a:battler::fire_resist,X
	AND #$00FF
	TAX
	LDA @LOCAL00
	JSR CALC_RESIST_DAMAGE
	JSR WEAKEN_SHIELD
@UNKNOWN0:
	END_C_FUNCTION
