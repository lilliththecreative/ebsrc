
CHANGE_MUSIC:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_PARAM_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	END_STACK_VARS
	TAX
	STX @LOCAL02
	CPX CURRENT_MUSIC_TRACK
	BEQL @RETURN
	LDA DISABLED_TRANSITIONS
	BNE @UNKNOWN1
	JSL PLAY_SOUND_UNKNOWN0
@UNKNOWN1:
	LDX @LOCAL02
	; Sound stone recordings should play seamlessly
	CPX #MUSIC::SOUNDSTONE_RECORDING_GIANT_STEP
	BCC @STOP_MUSIC
	CPX #MUSIC::SOUNDSTONE_RECORDING_FIRE_SPRING
	BLTEQ @DONT_STOP_MUSIC
@STOP_MUSIC:
	LDA #$0001
	JSL UNKNOWN_C0AC0C
	JSL STOP_MUSIC
@DONT_STOP_MUSIC:
	LDX @LOCAL02
	STX CURRENT_MUSIC_TRACK
	TXY
	DEY
	STY @LOCAL01
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(pack_table_entry)
	TAX
	LDA f:MUSIC_DATASET_TABLE,X ;pack_table_entry::primary_sample_pack
	AND #$00FF
	STA @LOCAL00
	CMP CURRENT_PRIMARY_SAMPLE_PACK
	BEQ @SKIP_DATA_LOAD_1
	CMP #$00FF
	BEQ @SKIP_DATA_LOAD_1
	STA CURRENT_PRIMARY_SAMPLE_PACK
	LOADPTR MUSIC_PACK_POINTER_TABLE, @VIRTUAL06
	LDA @LOCAL00
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(music_pack_pointer)
	STA @VIRTUAL02
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A] ;music_pack_pointer::bank
	AND #$00FF
	JSR UNKNOWN_C4FB42
	TAX
	LDA @VIRTUAL02
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06] ;music_pack_pointer::addr
	AND SEQUENCE_PACK_MASK
	JSL LOAD_SPC700_DATA
@SKIP_DATA_LOAD_1:
	LDY @LOCAL01
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(pack_table_entry)
	TAX
	INX
	LDA f:MUSIC_DATASET_TABLE,X ;pack_table_entry::secondary_sample_pack
	AND #$00FF
	STA @LOCAL00
	CMP CURRENT_SECONDARY_SAMPLE_PACK
	BEQ @SKIP_DATA_LOAD_2
	CMP #$00FF
	BEQ @SKIP_DATA_LOAD_2
	CMP UNKNOWN_7EB543
	BEQ @SKIP_DATA_LOAD_2
	STA CURRENT_SECONDARY_SAMPLE_PACK
	LOADPTR MUSIC_PACK_POINTER_TABLE, @VIRTUAL06
	LDA @LOCAL00
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(music_pack_pointer)
	STA @VIRTUAL02
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A] ;music_pack_pointer::bank
	AND #$00FF
	JSR UNKNOWN_C4FB42
	TAX
	LDA @VIRTUAL02
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06] ;music_pack_pointer::addr
	AND SEQUENCE_PACK_MASK
	JSL LOAD_SPC700_DATA
@SKIP_DATA_LOAD_2:
	LDY @LOCAL01
	TYA
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(pack_table_entry)
	TAX
	INX
	INX
	LDA f:MUSIC_DATASET_TABLE,X ;pack_table_entry::sequence_pack
	AND #$00FF
	STA @LOCAL00
	CMP CURRENT_SEQUENCE_PACK
	BEQ @SKIP_DATA_LOAD_3
	CMP #$00FF
	BEQ @SKIP_DATA_LOAD_3
	STA CURRENT_SEQUENCE_PACK
	LOADPTR MUSIC_PACK_POINTER_TABLE, @VIRTUAL06
	LDA @LOCAL00
	OPTIMIZED_MULT @VIRTUAL04, .SIZEOF(music_pack_pointer)
	STA @VIRTUAL02
	MOVE_INTX @VIRTUAL06, @VIRTUAL0A
	CLC
	ADC @VIRTUAL0A
	STA @VIRTUAL0A
	LDA [@VIRTUAL0A] ;music_pack_pointer::bank
	AND #$00FF
	JSR UNKNOWN_C4FB42
	TAX
	LDA @VIRTUAL02
	INC
	CLC
	ADC @VIRTUAL06
	STA @VIRTUAL06
	LDA [@VIRTUAL06] ;music_pack_pointer::addr
	AND SEQUENCE_PACK_MASK
	JSL LOAD_SPC700_DATA
@SKIP_DATA_LOAD_3:
	LDY @LOCAL01
	TYA
	INC
	JSL UNKNOWN_C0ABBD
@RETURN:
	END_C_FUNCTION
