
PLAY_SOUND:
	SEP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	CMP #$0000
	BEQ PLAY_SOUND_UNKNOWN0
	LDX SOUND_EFFECT_QUEUE_END_INDEX
	ORA SOUND_EFFECT_UPPER_BIT_FLIPPER
	STA SOUND_EFFECT_QUEUE,X
	TXA
	INC
	AND #$0007
	STA SOUND_EFFECT_QUEUE_END_INDEX
	LDA #$0080
	EOR SOUND_EFFECT_UPPER_BIT_FLIPPER
	STA SOUND_EFFECT_UPPER_BIT_FLIPPER
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	RTL
PLAY_SOUND_UNKNOWN0:
	SEP #PROC_FLAGS::ACCUM8
	LDA #$0057
	STA f:APUIO3
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8
	RTL
