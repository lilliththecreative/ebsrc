
COPY_SAVE:
	BEGIN_C_FUNCTION_FAR
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int to
	STACK_RESERVE_PARAM_INT16 ;int from
	END_STACK_VARS
	STA @LOCAL01
	TXA
	ASL
	TAY
	STY @LOCAL00
	LDA @LOCAL01
	ASL
	STA @VIRTUAL02
	TYX
	LDA @VIRTUAL02
	JSR COPY_SAVE_BLOCK
	LDY @LOCAL00
	TYX
	INX
	LDA @VIRTUAL02
	INC
	JSR COPY_SAVE_BLOCK
	END_C_FUNCTION
