
CHECK_SAVE_CORRUPTION:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int slot
	END_STACK_VARS
	TAY
	STY @LOCAL01
	TYA
	ASL
	STA @VIRTUAL02
	JSR VALIDATE_SAVE_BLOCK_CHECKSUMS
	CMP #FALSE
	BEQ @UNKNOWN1
	LDA @VIRTUAL02
	JSR ERASE_SAVE_BLOCK
	LDX @VIRTUAL02
	INX
	STX @LOCAL00
	TXA
	JSR VALIDATE_SAVE_BLOCK_CHECKSUMS
	CMP #FALSE
	BEQ @UNKNOWN0
	LDX @LOCAL00
	TXA
	JSR ERASE_SAVE_BLOCK
	LDY @LOCAL01
	TYX
	SEP #PROC_FLAGS::ACCUM8
	LDA f:UNKNOWN_EF05A6,X
	ORA CORRUPTION_CHECK_RESULTS
	STA CORRUPTION_CHECK_RESULTS
	BRA @UNKNOWN2
@UNKNOWN0:
	LDX @LOCAL00
	LDA @VIRTUAL02
	JSR COPY_SAVE_BLOCK
@UNKNOWN1:
	LDY @VIRTUAL02
	INY
	STY @LOCAL01
	TYA
	JSR VALIDATE_SAVE_BLOCK_CHECKSUMS
	.A16
	CMP #FALSE
	BEQ @UNKNOWN2
	LDY @LOCAL01
	TYA
	JSR ERASE_SAVE_BLOCK
	LDX @VIRTUAL02
	LDY @LOCAL01
	TYA
	JSR COPY_SAVE_BLOCK
@UNKNOWN2:
	REP #PROC_FLAGS::ACCUM8
	END_C_FUNCTION
