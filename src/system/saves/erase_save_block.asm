
ERASE_SAVE_BLOCK:
	REP #PROC_FLAGS::ACCUM8 | PROC_FLAGS::INDEX8 | PROC_FLAGS::CARRY
	STACK_RESERVE_VARS
	STACK_RESERVE_INT32
	STACK_RESERVE_INT32
	STACK_RESERVE_INT16
.IF !.DEFINED(JPN)
	STACK_RESERVE_INT32
.ENDIF
	STACK_RESERVE_PARAM_INT16
	END_STACK_VARS
	LDY #$0500
	JSL MULT16
	STORE_INT1632 $0A
	CLC
	VAR_ADD_CONST_INT_ASSIGN SAVE_BASE, $0A
.IF .DEFINED(JPN)
	MOVE_INT $0A, $0E
.ELSE
	MOVE_INT $0A, $06
	MOVE_INT $06, $0E
.ENDIF
	LDX #$0500
	SEP #PROC_FLAGS::ACCUM8
	LDA #$0000
	JSL MEMSET24
	.A16
	LOADPTR SRAM_SIGNATURE, $06
.IF !.DEFINED(JPN)
	MOVE_INT $06, $18
.ENDIF
	MOVE_INT $06, $0E
	JSL STRLEN
	STA $16
.IF .DEFINED(JPN)
	MOVE_INT $0A, $0E
.ELSE
	MOVE_INT $0A, $06
	MOVE_INT $06, $0E
	MOVE_INT $18, $06
.ENDIF
	MOVE_INT $06, @LOCAL01
	LDA $16
	JSL MEMCPY24
	PLD
	RTS
