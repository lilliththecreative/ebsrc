; Do nothing for some number of frames
WAIT:
	BEGIN_C_FUNCTION
	STACK_RESERVE_VARS
	STACK_RESERVE_INT16
	STACK_RESERVE_PARAM_INT16 ;int frames
	END_STACK_VARS
	TAX
	STX @LOCAL00
	BRA @UNKNOWN1
@UNKNOWN0:
	JSL WINDOW_TICK
@UNKNOWN1:
	LDX @LOCAL00
	TXA
	DEX
	STX @LOCAL00
	CMP #0
	BNE @UNKNOWN0
	END_C_FUNCTION
